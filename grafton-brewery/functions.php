/**
 * Set the content width in pixels, based on the theme's design and stylesheet.
 *
 * Priority 0 to make it available to lower priority callbacks.
 *
 * @global int $content_width
 */
function grafton_brewery_content_width() {
	$GLOBALS['content_width'] = apply_filters( 'grafton_brewery_content_width', 640 );
}
add_action( 'after_setup_theme', 'grafton_brewery_content_width', 0 );


/**
 * Enqueue scripts and styles.
 */
function grafton_brewery_scripts() {
	wp_enqueue_style( 'grafton-brewery-style', get_stylesheet_uri(), array(), _S_VERSION );
	wp_style_add_data( 'grafton-brewery-style', 'rtl', 'replace' );

	wp_enqueue_script( 'grafton-brewery-navigation', get_template_directory_uri() . '/js/navigation.js', array(), _S_VERSION, true );

	if ( is_singular() && comments_open() && get_option( 'thread_comments' ) ) {
		wp_enqueue_script( 'comment-reply' );
	}
}
add_action( 'wp_enqueue_scripts', 'grafton_brewery_scripts' );



