function custom_confirmation_message( $confirmation, $form, $entry, $ajax ) {
    if ( ! is_string( $confirmation ) ) {
        return $confirmation;
    }
 
    $confirmation .= GFCommon::get_inline_script_tag( "window.top.jQuery(document).on('gform_confirmation_loaded', function () { setTimeout(() => {
		document.querySelector('.sign-up-popup').style.display = 'none';
	  }, '2000'); } );" );
 
    return $confirmation;
}